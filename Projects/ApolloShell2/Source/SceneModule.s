;----------------------------------------------------------
; SceneModule_Init
; Initializes the scene module controller
;----------------------------------------------------------
SceneModule_Init:

	movem.l	d0-d1/a0,-(SP)

	movem.l (SP)+,d0-d1/a0
	rts

;----------------------------------------------------------
; SceneModule_Process
; Processes the scene module controller
;----------------------------------------------------------
SceneModule_Process:

	movem.l	d0-d1/a0,-(SP)

	movem.l (SP)+,d0-d1/a0
	rts

;----------------------------------------------------------
; SceneModule_Exit
; Tidy exit for the scene module controller
;----------------------------------------------------------
SceneModule_Exit:

	movem.l	d0-d1/a0,-(SP)

	movem.l (SP)+,d0-d1/a0
	rts

