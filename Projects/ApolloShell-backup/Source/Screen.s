	SECTION screen,CODE_C

;-----------------------------------------------------------------------------

;----------------------------------------------------------
; Screen_Clear
; Clears the screen, currently fixed to a light green
;----------------------------------------------------------
Screen_Clear:

	movem.l	d0-d1/a0,-(SP)

	move.l	#SCREENCLEARCOLOUR,d1
	move.l	#((SCREENWIDTH*SCREENHEIGHT)/4)-1,d0
	move.l	screenPtr,a0

.loop:	

	move.l	d1,(a0)+
	dbra	d0,.loop

	movem.l (SP)+,d0-d1/a0
	rts



